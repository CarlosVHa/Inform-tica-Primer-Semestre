#Dado un numero del 1 al 7 mostrar su equivalente en literal
n=int(input("Ingrese un numero entre 1 y 7"))
if n==1:
    print("UNO")
elif n==2:
    print("DOS")
elif n==3:
    print("TRES")
elif n==4:
    print("CUATRO")
elif n==5:
    print("CINCO")
elif n==6:
    print("SEIS")
elif n==7:
    print("SIETE")
else:
    print("El numero debe ser entre 1 a 7")